        /// <summary>SCENARIO-MATRIX.md from the scenario catalog.</summary>
        public static string ScenarioMatrix(IReadOnlyList<ScenarioInfo> scenarios)
        {
            ArgumentNullException.ThrowIfNull(scenarios);
            var sb = new StringBuilder(Header);
            sb.Append("# STS2 Scenario Matrix\n\n");
            sb.Append("Total: ").Append(scenarios.Count).Append(" (");
            sb.Append(string.Join(", ", scenarios.GroupBy(s => s.Status).OrderBy(g => g.Key, StringComparer.Ordinal)
                .Select(g => g.Count() + " " + g.Key)));
            sb.Append(").\n\nBy milestone: ");
            sb.Append(string.Join(", ", scenarios.GroupBy(s => s.Milestone).OrderBy(g => g.Key, StringComparer.Ordinal)
                .Select(g => g.Key + "=" + g.Count())));
            sb.Append(". By adapter: ");
            sb.Append(string.Join(", ", scenarios.GroupBy(s => s.Adapter).OrderBy(g => g.Key, StringComparer.Ordinal)
                .Select(g => g.Key + "=" + g.Count())));
            sb.Append(".\n\n| Scenario | Tags | Status | Milestone | Adapter |\n|---|---|---|---|---|\n");
            foreach (ScenarioInfo s in scenarios.OrderBy(s => s.Name, StringComparer.Ordinal))
            {
                sb.Append("| ").Append(s.Name)
                  .Append(" | ").Append(string.Join(", ", s.Tags))
                  .Append(" | ").Append(s.Status)
                  .Append(" | ").Append(s.Milestone)
                  .Append(" | ").Append(s.Adapter).Append(" |\n");
            }
            sb.Append("\nMultiplexer-layer scenarios are realized as unit/E2E tests (SPEC §14.2) and tracked here with adapter=multiplexer so corpus accounting stays honest.\n");
            return sb.ToString();
        }

