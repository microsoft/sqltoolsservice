        private const string Header = "<!-- GENERATED by Microsoft.SqlTools.Sts2.Testing.GeneratedDocs; do not edit by hand. Regenerate: ./scripts/update-sts2-docs.ps1 -->\n";

        /// <summary>File name to generator content, for every doc.</summary>
        public static IReadOnlyDictionary<string, string> All(string repoRoot) => new SortedDictionary<string, string>(StringComparer.Ordinal)
        {
            ["CONTRACT.md"] = Contract(),
            ["TRACE-SCHEMA.md"] = TraceSchema(),
            ["INVARIANTS.md"] = Invariants(),
            ["SCENARIO-MATRIX.md"] = ScenarioMatrix(ScenarioCatalog.Load(Path.Combine(repoRoot, "test", "sts2", "scenarios"))),
            ["STATE-MACHINE.md"] = StateMachine(),
            ["COMPONENTS.md"] = Components(Path.Combine(repoRoot, "src", "sts2")),
        };

        /// <summary>CONTRACT.md: methods, error codes, wire constants.</summary>
        public static string Contract()
        {
            var sb = new StringBuilder(Header);
            sb.Append("# STS2 Wire Contract\n\n");
            sb.Append("Spec version: `").Append(Sts2WireConstants.SpecVersion).Append("`. Method prefix: `")
              .Append(Sts2WireConstants.MethodPrefix).Append("`.\n\n");

            sb.Append("## Methods\n\n| Method | Kind | Milestone | Summary |\n|---|---|---|---|\n");
            foreach (Sts2MethodInfo method in Sts2Methods.All)
            {
                sb.Append("| `").Append(method.Name).Append(method.Toy ? "` (toy)" : "`")
                  .Append(" | ").Append(method.Kind)
                  .Append(" | ").Append(method.Milestone)
                  .Append(" | ").Append(method.Summary).Append(" |\n");
            }

            sb.Append("\n## Stable error codes (`error.data.code`)\n\n| Code | Constant |\n|---|---|\n");
            foreach (FieldInfo field in typeof(Sts2ErrorCodes)
                         .GetFields(BindingFlags.Public | BindingFlags.Static)
                         .Where(f => f.IsLiteral)
                         .OrderBy(f => f.Name, StringComparer.Ordinal))
            {
                sb.Append("| `").Append((string)field.GetRawConstantValue()!).Append("` | Sts2ErrorCodes.").Append(field.Name).Append(" |\n");
            }
            sb.Append("\nJSON-RPC `error.code` is always numeric (I12); these strings are the stable identity.\n");
            return sb.ToString();
        }

        /// <summary>TRACE-SCHEMA.md: envelope schema, kinds, redaction markers.</summary>
        public static string TraceSchema()
        {
            var sb = new StringBuilder(Header);
            sb.Append("# STS2 Trace Schema\n\n");
            sb.Append("Envelope schema id: `").Append(EnvelopeJsonCodec.SchemaId).Append("` (SPEC §8.1). Journal lines are single-line JSON with this field order:\n\n");
            sb.Append("| Field | Type | Meaning |\n|---|---|---|\n");
            sb.Append("| schema | string | envelope schema identifier |\n");
            sb.Append("| runId | string | stable per process run |\n");
            sb.Append("| seq | number | gapless monotonic, assigned by the coordinator (I5) |\n");
            sb.Append("| ts | string | UTC round-trip (Z); replay uses recorded values |\n");
            sb.Append("| kind | string | one of the kinds below |\n");
            sb.Append("| sessionId | string? | logical connection/session id |\n");
            sb.Append("| corr | string? | JSON-RPC id, effect id, or correlation id |\n");
            sb.Append("| cause | number? | seq of the producing envelope; null only for external inbound and root control |\n");
            sb.Append("| type | string | method, command, event, effect, metric, or diagnostic name |\n");
            sb.Append("| configVersion | number | config snapshot version |\n");
            sb.Append("| digest | string | sha256 of the canonical payload, present even when elided |\n");
            sb.Append("| payload | any? | inline payload when capture permits |\n");
            sb.Append("| payloadMeta | any? | redaction/elision metadata |\n");

            sb.Append("\n## Kinds\n\n");
            sb.Append("Whether each kind is emitted in v2.0, and by what. \"Reserved\" kinds are part of the closed schema (SPEC §8.1) but are not produced in v2.0; they are listed so the schema is honest about what a trace can actually contain.\n\n");
            sb.Append("| Kind | Status | Emitted by |\n|---|---|---|\n");
            foreach (string kind in EnvelopeKinds.All.OrderBy(k => k, StringComparer.Ordinal))
            {
                (string status, string by) = KindEmission(kind);
                sb.Append("| `").Append(kind).Append("` | ").Append(status).Append(" | ").Append(by).Append(" |\n");
            }

            sb.Append("\n## Redaction markers\n\n");
            sb.Append("Redacted scalar: `{\"$redacted\":true,\"kind\":\"sql\",\"digest\":\"sha256:...\",\"bytes\":N}` — the wrapper carries the original scalar's authoritative digest (SPEC §8.2).\n");
            sb.Append("Secret token: `secret:ref:<32-hex>:<counter>` — an OPAQUE random reference (derived from no part of the secret); real material lives only in the in-memory side table (SPEC §8.5).\n");
            sb.Append("Canonical payload form: UTF-8, ordinal-sorted keys, no insignificant whitespace, number tokens verbatim (D-0007).\n");
            return sb.ToString();
        }

        /// <summary>Emission status of one envelope kind in v2.0 (SPEC §8.1, §18.10 honesty).</summary>
        private static (string Status, string By) KindEmission(string kind) => kind switch
        {
            EnvelopeKinds.RpcInRequest => ("emitted", "inbound JSON-RPC requests (coordinator)"),
            EnvelopeKinds.RpcInNotify => ("emitted", "inbound JSON-RPC notifications (coordinator)"),
            EnvelopeKinds.RpcOutResult => ("emitted", "Core results (rpc.out.result)"),
            EnvelopeKinds.RpcOutError => ("emitted", "Core errors (rpc.out.error)"),
            EnvelopeKinds.RpcOutNotify => ("emitted", "Core notifications (query.rows, query.complete, ...)"),
            EnvelopeKinds.EffectRequest => ("emitted", "Core effect requests (driver.*, diag.export)"),
            EnvelopeKinds.EffectResponse => ("emitted", "effect-runner observations (driver.queryEvent, ...)"),
            EnvelopeKinds.Control => ("emitted", "session.start, lifecycle.shutdown/exit"),
            EnvelopeKinds.Diagnostic => ("emitted", "Core diagnostics (core.unexpectedInput)"),
            EnvelopeKinds.ConfigChanged => ("emitted", "setCapture (config change, I15)"),
            EnvelopeKinds.Metric => ("emitted (opt-in)", "coordinator metric snapshots when MetricSampleEvery > 0; the live metrics channel always tallies"),
            EnvelopeKinds.Command => ("reserved", "no internal command bus in v2.0"),
            EnvelopeKinds.Event => ("reserved", "query events travel as effect.res (driver.queryEvent), not evt"),
            EnvelopeKinds.TimerDue => ("reserved", "open/close timeouts run inside the effect runner, not as journaled timers"),
            EnvelopeKinds.StateSnapshot => ("reserved", "replay-shortcut snapshot optimization, not produced in v2.0"),
            _ => ("reserved", "-"),
        };

