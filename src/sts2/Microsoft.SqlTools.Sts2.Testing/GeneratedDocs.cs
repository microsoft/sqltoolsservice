        /// <summary>COMPONENTS.md from COMPONENT.md fragments plus actual csproj references.</summary>
        public static string Components(string sts2SourceDir)
        {
            var sb = new StringBuilder(Header);
            sb.Append("# STS2 Components\n\n");
            sb.Append("Roles come from each project's COMPONENT.md; references are read from the csproj files on disk. The allowed matrix is SPEC §4; DependencyMatrixTests enforces it (I11).\n\n");

            foreach (string projectDir in Directory.EnumerateDirectories(sts2SourceDir).Order(StringComparer.Ordinal))
            {
                string projectName = Path.GetFileName(projectDir);
                string csprojPath = Path.Combine(projectDir, projectName + ".csproj");
                if (!File.Exists(csprojPath))
                {
                    continue;
                }

                string role = "(no COMPONENT.md)";
                string componentPath = Path.Combine(projectDir, "COMPONENT.md");
                if (File.Exists(componentPath))
                {
                    role = File.ReadLines(componentPath)
                        .FirstOrDefault(l => l.StartsWith("**Role:**", StringComparison.Ordinal))
                        ?.Replace("**Role:**", string.Empty).Trim() ?? role;
                }

                XDocument csproj = XDocument.Load(csprojPath);
                string[] projectRefs = csproj.Descendants("ProjectReference")
                    .Select(r => Path.GetFileNameWithoutExtension(r.Attribute("Include")!.Value.Replace('\\', '/')))
                    .Order(StringComparer.Ordinal).ToArray();
                string[] packageRefs = csproj.Descendants("PackageReference")
                    .Select(r => (r.Attribute("Include") ?? r.Attribute("Update"))!.Value)
                    .Order(StringComparer.Ordinal).ToArray();

                sb.Append("## ").Append(projectName).Append("\n\n");
                sb.Append(role).Append("\n\n");
                sb.Append("- Project references: ").Append(projectRefs.Length == 0 ? "none" : string.Join(", ", projectRefs)).Append('\n');
                sb.Append("- Package references: ").Append(packageRefs.Length == 0 ? "none (BCL only)" : string.Join(", ", packageRefs)).Append("\n\n");
            }
            return sb.ToString();
        }
